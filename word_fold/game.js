let foundWords = [];

function updateWordBank() {
    const wordBank = document.getElementById("words");
    if (!wordBank) return;

    const currentBoard = boards[level];
    wordBank.innerHTML = ""; // Clear old words

    currentBoard.words.forEach(word => {
        const wordSpan = document.createElement("span");
        wordSpan.textContent = word;
        if (foundWords.includes(word)) {
            wordSpan.className = "word-found";
        }
        wordBank.appendChild(wordSpan);
        wordBank.appendChild(document.createTextNode(" ")); // Add space between words
    });
}

function check_for_win(word) {
    const currentBoard = boards[level];
    // Check if the word is in the list and hasn't been found yet
    if (currentBoard.words.includes(word) && !foundWords.includes(word)) {
        foundWords.push(word);
        updateWordBank();

        // Specific animation for CYAN on the easy level (level 0)
        if (word === "CYAN" && level === 0) {
            const animationElement = document.getElementById('cyan-animation');
            if (animationElement) {
                animationElement.style.display = 'block';
                setTimeout(() => { animationElement.style.display = 'none'; }, 3000); // 3 seconds
            }
        }
    }
}

function make_cell_list() {
    let cells = [...document.getElementById("cell-holder").children];
    let cell_board = [];
    for (let i = 0; i < 25; i += 5) {
        cell_board.push(cells.slice(i, i + 5))
    }
    return cell_board;
}

function setup_game(board_index) {
    foundWords = []; // Reset found words for new game
    const board = boards[board_index];
    for (let x = 0; x < 5; x++) {
        for (let y = 0; y < 5; y++) {
            CELLS[y][x].innerHTML = board.cells[y][x];
        }
    }
    updateWordBank();
}

const CELLS = make_cell_list();
let selected_x = -1;
let selected_y = -1;

function move(x, y) {
    const combinedWord = CELLS[selected_y][selected_x].innerHTML + CELLS[y][x].innerHTML;
    CELLS[y][x].innerHTML = combinedWord;
    CELLS[selected_y][selected_x].innerHTML = ""
    check_for_win(combinedWord);
    select(x, y);
}

function unselect(x, y) {
    CELLS[y][x].classList.remove("selected");
    selected_x = -1;
    selected_y = -1;
}

function select(x, y) {
    if (CELLS[y][x].innerHTML.length > 0) {
        if (selected_x >= 0 && selected_y >= 0)
            CELLS[selected_y][selected_x].classList.remove("selected");
        CELLS[y][x].classList.add("selected");
        selected_y = y;
        selected_x = x;
    }
}

function is_close(a, b) {
    return Math.abs(a - b) <= 1
}

function can_move(x, y) {
    let can_move = is_close(selected_x, x) && selected_y == y || is_close(selected_y, y) && selected_x == x;

    return selected_x >= 0 && selected_y >= 0 && can_move && CELLS[y][x].innerHTML.length > 0
}

function on_click(x, y) {
    if (selected_x == x && selected_y == y) {
        unselect(x, y)
    }
    else if (can_move(x, y)) {
        move(x, y)
    } else {
        select(x, y)
    }
}

